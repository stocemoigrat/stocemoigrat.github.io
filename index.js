// add a game card to the collection view
function addGame(game, index) {
	collection.innerHTML += '<div class="card" id="' + index + '" onclick="openWindow(this)" style="background-image: url(\'images/' + game.imageName + '\');' + game.imagePosition + '"><div class="cardInfo w' + Math.floor(game.weight) + '"><div><i class="material-icons">access_time</i>' + game.timeToPlay + '</div><div>' + game.type + '</div><div><i class="material-icons">extension</i>' + game.weight + '</div></div></div>';
}


var playerCountDropdown = document.getElementById("playerCountDropdown");
var playerCountLabel = document.getElementById("playerCountLabel");

// filter by player count
function filterSelection(count) {
	playerCountLabel.innerHTML = count;
	if (count == "8+") count = 8;
	let cards = document.getElementsByClassName("card");
	
	for (let i = 0; i < cards.length; i++) {
		if (count == 'any' || games[cards[i].id].recPlayerCount.includes(count)) {
			cards[i].style.display = 'inline-block';
		} else {
			cards[i].style.display = 'none';
		}
	}
	toggleDropdown();
}
	
function toggleDropdown() {
	if (playerCountDropdown.style.display == 'none') {
		playerCountDropdown.style.display = 'block';
	} else {
		playerCountDropdown.style.display = 'none';
	}
}

// sort by time or weight
sortGames('time');
sortGames('weight');

function sortGames(sortBy) {
	let toSort = Array.prototype.slice.call(collection.children, 0); //  turn NodeList to array
	
	if (sortBy == 'time') {
		toSort.sort(function(a, b) {
			return games[a.id].timeToPlayNum - games[b.id].timeToPlayNum;
		});
	} else if (sortBy == 'weight') {
		toSort.sort(function(a, b) {
			return games[a.id].weight - games[b.id].weight;
		});
	}

	collection.innerHTML = "";
	for(var i = 0, l = toSort.length; i < l; i++) {
	collection.appendChild(toSort[i]);
	}
}
function sortButtonClicked(sortBy, clickedButton) {
	let buttons = clickedButton.parentElement.children;
	for (let i = 0; i < buttons.length; i++) {
		buttons[i].classList.remove('active');
	}
	clickedButton.classList.add('active');
	sortGames(sortBy);
}

var infoWindow = document.getElementById("fullInfoWindow");
var infoWindowName = document.getElementById("name");
var infoWindowCover = document.getElementById("cover");
var infoWindowPhoto = document.getElementById("photo");
var infoWindowWeight = document.getElementById("weight");
var infoWindowTimeToLearn = document.getElementById("timeToLearn");
var infoWindowTimeToPlay = document.getElementById("timeToPlay");
var playerCounts = document.getElementById("playerCount").children;
var infoWindowExpansions = document.getElementById("expansions");
var infoWindowTags = document.getElementById("tags");
var infoWindowDescription = document.getElementById("description");

function openWindow(card) {
	let game = games[card.id];
	
	infoWindowName.innerHTML = game.name;
	
	infoWindowCover.innerHTML = '<img src="images/' + game.imageName + '">';
	infoWindowPhoto.src = 'setups/' + game.imageName;

	switch(Math.floor(game.weight)) {
		case 1:
			infoWindowWeight.innerHTML = 'light';
			infoWindowWeight.className = 'box weight1';
			break;
		case 2:
			infoWindowWeight.innerHTML = 'medium-light';
			infoWindowWeight.className = 'box weight2';
			break;
		case 3:
			infoWindowWeight.innerHTML = 'medium';
			infoWindowWeight.className = 'box weight3';
			break;
		case 4:
			infoWindowWeight.innerHTML = 'medium-heavy';
			infoWindowWeight.className = 'box weight4';
			break;
		case 5:
			infoWindowWeight.innerHTML = 'heavy';
			infoWindowWeight.className = 'box weight5';
			break;
		default:
			infoWindowWeight.innerHTML = 'error';
	}

	infoWindowTimeToLearn.innerHTML = game.timeToLearn;
	infoWindowTimeToPlay.innerHTML = game.timeToPlay;

	playerCounts[0].innerHTML = '<i class="material-icons">group</i> ' + game.playerCount[0] + (game.playerCount.length > 1 ? '-' + game.playerCount[game.playerCount.length - 1] : '');
	playerCounts[1].innerHTML = '<i class="material-icons">group</i> ' + game.recPlayerCount[0] + (game.recPlayerCount.length > 1 ? '-' + game.recPlayerCount[game.recPlayerCount.length - 1] : '');
	playerCounts[2].innerHTML = '<i class="material-icons">group</i> ' + game.bestPlayerCount[0] + (game.bestPlayerCount.length > 1 ? '-' + game.bestPlayerCount[game.bestPlayerCount.length - 1] : '');
	
	if (game.expansions.length) {
		infoWindowExpansions.parentElement.style.display = 'inline';
		infoWindowExpansions.innerHTML = '';
		game.expansions.forEach( x => {infoWindowExpansions.innerHTML += '&bull; ' + x + '<br>'; });
	} else {
		infoWindowExpansions.parentElement.style.display = 'none';
	}
	
	infoWindowTags.innerHTML = '';
	if (game.coop) infoWindowTags.innerHTML += '<span>cooperative</span>';
	game.tags.forEach(tag => {infoWindowTags.innerHTML += '<span>' + tag + '</span>';});
	
	infoWindowDescription.innerHTML = game.description;
	
	infoWindow.style.display = 'flex';
}

function closeWindow() {
	infoWindow.style.display = 'none';
}

document.querySelector('body').onclick = function(event) {
	if (event.target == infoWindow) {
		infoWindow.style.display = 'none';
	} else if (playerCountDropdown.style.display != 'none' && !playerCountDropdown.parentElement.contains(event.target)) {
		playerCountDropdown.style.display = 'none';
	}
}

